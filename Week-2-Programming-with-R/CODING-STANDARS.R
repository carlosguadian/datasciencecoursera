# CODING STANDARS FOR R

# Siempre utilizar un editor de texto / código
# 
# Tabular el código, ayudará a que lea mejor. Las posiciones de inicio de las líneas
# indicarán donde empiezan y acaban los bloques de programación.
# 
# Limita la anchura del código. Una anchura de 80 está bien.
# 
